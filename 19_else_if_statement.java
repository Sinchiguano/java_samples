class else_if_statement
{
	public static void main(String [] args)
	{
		int age=55;
		if(age>=60)
		{
			System.out.println("U are very old");
		}
		else if(age>=40)
		{
			System.out.println("U are in your 40");
		}
		else
		{
			System.out.println("U are a young buck...");
		}
	}
}
