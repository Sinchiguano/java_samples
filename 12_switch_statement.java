class switch_statement
{
	public static void main(String args[])
	{
		int age=3;
		switch (age) 
		{
			case 1:
				System.out.println("U can crawl...");
				break;
			case 2:
				System.out.println("");
			default:
				System.out.println("I don't know how old u are....");
				break;
		}
	}
}



